            1. 系统登录：<br>
            本系统用户分为访客、普通用户和管理员三类。
            其中，游客不需要登录，且权限最小，只能浏览新闻发布系统首页。
            普通用户与管理员需各自输入正确的用户名、密码及验证码登录。
            其中，管理员权限大于用户，具备添加新闻分类、删除与编辑新闻内容等额外的权限。
            用户登录后，登录状态将保持一小时。<br>
            2. 用户权限主要功能：<br>
            (1)新闻发布：用户可以发布新闻，输入标题和内容及所属栏目，其中上传图片大小将受到限制。新闻内容经管理员审核后将显示于首页。 <br>
            (2)评论发布：在浏览新闻的同一页面结尾，用户可发布相关评论。浏览页面将显示评论内容、评论日期及IP地址。<br>
            (3)评论浏览：在浏览新闻的同一页面结尾，用户可浏览新闻相关的评论。若发现违法违规言论，可在页面底部点击“举报违法信息”。进行举报。<br>
            (4)分类浏览：分类浏览页面将新闻进行分类呈现，便于用户根据自身兴趣浏览。<br>
            3. 管理员权限额外功能：<br>
            (1)新闻分类：管理员可于“添加分类”页面添加新闻分类与分类描述，丰富新闻发布系统内容。<br>
            (2)新闻审核：管理员可审核用户将发布的新闻内容以及删除、编辑用户已发布的新闻内容，维护系统生态环境。<br>
